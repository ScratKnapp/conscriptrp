<font color='red' size='6'>Written by 毛泽东.</font>

<font color='white' size='4'>Synopsis</font>
Quotations from Chairman Mao Tse-Tung consists of 427 quotations, organized thematically into 33 chapters. 
It is also called "Thoughts of Chairman Mao" by many Chinese people. 
The quotations range in length from a sentence to a few short paragraphs, and borrow heavily from a group of about two dozen documents in the four volumes of Mao's Selected Works.

Usually the quotations are arranged logically, to deal with one to three themes in the development of a chapter. The table below summarizes the book.

<font color='grey' size='3'>The Communist Party</font>
{Chapter 1 - Quotations 13}
The Chinese Communist Party is the core of the Chinese revolution, and its principles are based on Marxism–Leninism. 
Party criticism should be carried out within the Party.

<font color='grey' size='3'>Classes and Class Struggle</font>
{Chapter 2 - Quotations 22}
The revolution, and the recognition of class and class struggle, are necessary for peasants and the Chinese people to overcome both domestic and foreign enemy elements. 
This is not a simple, clean, or quick struggle.

<font color='grey' size='3'>Socialism and Communism</font>
{Chapter 3 - Quotations 28}
Socialism must be developed in China, and the route toward such an end is a democratic revolution, which will enable socialist and communist consolidation over a length of time. 
It is also important to unite with the middle peasants, and educate them on the failings of capitalism.

<font color='grey' size='3'>The Correct Handling of Contradictions Among the People</font>
{Chapter 4 - Quotations 16}
There are at least two basic kinds of contradiction: the antagonistic contradictions which exist between communist countries and their capitalist neighbors and between the people and the enemies of the people, and the contradictions among the people themselves, people unconvinced of China's new path, which should be dealt with in a democratic and non-antagonistic fashion.

<font color='grey' size='3'>War and Peace</font>
{Chapter 5 - Quotations 21}
War is a continuation of politics, and there are at least two types: just (progressive) and unjust wars, which only serve bourgeois interests. 
While no one likes war, we must remain ready to wage just wars against imperialist agitations.

<font color='grey' size='3'>Imperialism and All Reactionaries Are Paper Tigers</font>
{Chapter 6 - Quotations 10}
U.S. imperialism, and European and domestic reactionary forces, represent real dangers, and in this respect are like real tigers. 
However, because the goal of Chinese communism is just, and reactionary interests are self-centered and unjust, after struggle, they will be revealed to be much less dangerous than they were earlier perceived to be.

<font color='grey' size='3'>Dare to Struggle and Dare to Win</font>
{Chapter 7 - Quotations 10}
Fighting is unpleasant, and the people of China would prefer not to do it at all. 
At the same time, they stand ready to wage a just struggle of self-preservation against reactionary elements, both foreign and domestic.

<font color='grey' size='3'>People's War</font>
{Chapter 8 - Quotations 10}
China's masses are the greatest conceivable weapon for fighting against Japanese imperialism and domestic reactionaries. 
Basic strategic points for war against the Kuomintang are also enumerated.

<font color='grey' size='3'>The People's Army</font>
{Chapter 9 - Quotations 8}
The People's Army is not merely an organ for fighting; it is also an organ for the political advancement of the Party, as well as of production.

<font color='grey' size='3'>Leadership of Party Committees</font>
{Chapter 10 - Quotations 14}
Internal life of the Party is discussed. 
Committees are useful to avoid monopolization by others, and Party members must demonstrate honesty, openness in discussing problems, and the ability to learn and multitask.

<font color='grey' size='3'>The Mass Line</font>
{Chapter 11 - Quotations 22}
The mass line represents the creative and productive energies of the masses of the Chinese population, which are potentially inexhaustible. 
Party members should take their cue from the masses, and reinterpret policy with respect to the benefit of the masses.

<font color='grey' size='3'>Political Work</font>
{Chapter 12 - Quotations 21}
It is necessary for intellectuals, students, soldiers and the average peasant to pay attention and involve themselves with political work. This is particularly true in wartime.

<font color='grey' size='3'>Relations Between Officers and Men</font>
{Chapter 13 - Quotations 7}
Non-antagonistic and democratic relations between officers and men make for a stronger army.

<font color='grey' size='3'>Relations Between the Army and the People</font>
{Chapter 14 - Quotations 6}
An army that is cherished and respected by the people, and vice versa, is a nearly invincible force. The army and the people must unite on the grounds of basic respect.

<font color='grey' size='3'>Democracy in the Three Main Fields</font>
{Chapter 15 - Quotations 8}
Democracy and honesty play roles in the reform of the army, as well as in the life of the Party, and of cadres. "Ultra-democracy", which is defined as an individualistic bourgeois aversion to discipline, is to be avoided.

<font color='grey' size='3'>Education and the Training of Troops</font>
{Chapter 16 - Quotations 9}
Education must have a practical and political basis for the army, Party and cadres. 
Along democratic lines, it will also be possible for the officers to teach the soldiers, for the soldiers to teach the officers, and for the soldiers to teach each other.

<font color='grey' size='3'>Serving the People</font>
{Chapter 17 - Quotations 9}
It is the duty of the cadres and the Party to serve the people. Without the people's interests constantly at heart, their work is useless.

<font color='grey' size='3'>Patriotism and Internationalism</font>
{Chapter 18 - Quotations 7}
The patriotism of a communist nation and an internationalist sympathy for just struggles in other countries are in no way exclusive; on the contrary, they are linked deeply, as communism spreads throughout the world. 
At the same time, it is important for a country to retain modesty, and shun arrogance.

<font color='grey' size='3'>Revolutionary Heroism</font>
{Chapter 19 - Quotations 8}
The same limitless creative energy of the masses is also visible in the army, in their fighting style and indomitable will.

<font color='grey' size='3'>Building Our Country Through Diligence and Frugality</font>
{Chapter 20 - Quotations 8}
China's road to modernization will be built on the principles of diligence and frugality. 
Nor will it be legitimate to relax if, 50 years later, modernization is realized on a mass scale.

<font color='grey' size='3'>Self-Reliance and Arduous Struggle</font>
{Chapter 21 - Quotations 13}
It is necessary for China to become self-reliant in the course of the revolution, along the usual lines of class struggle. 
At the same time, it is a mistake for individuals to only see the good or the bad in a system, to the exclusion of all else.

<font color='grey' size='3'>Methods of Thinking and Methods of Work</font>
{Chapter 22 - Quotations 41}
Marxist dialectical materialism, which connotes the constant struggle between opposites in an empirical setting, is the best method toward constant improvement. 
Objective analysis of problems based on empirical results is at a premium.

<font color='grey' size='3'>Investigation and Study</font>
{Chapter 23 - Quotations 9}
It is necessary to investigate both the facts and the history of a problem in order to study and understand it.

<font color='grey' size='3'>Correcting Mistaken Ideas</font>
{Chapter 24 - Quotations 15}
Arrogance, lack of achievement after a prosperous period, selfishness, shirking work, and liberalism, are all evils to be avoided in China's development. 
Liberalism is taken to mean that one may avoid conflict or work in order to be more comfortable for the moment, while the problem continues to grow.

<font color='grey' size='3'>Unity</font>
{Chapter 25 - Quotations 5}
Unity of the masses, the Party and the whole country is essential. At the same time, criticism may take place along comradely lines, while at the same time a basic unity is felt and preserved. This is the dialectical method.

<font color='grey' size='3'>Discipline</font>
{Chapter 26 - Quotations 5}
Discipline is seen not to be exclusive to democratic methods. Basic points of military conduct are also enumerated.

<font color='grey' size='3'>Criticism and Self-Criticism</font>
{Chapter 27 - Quotations 15}
Criticism is a part of the Marxist dialectical method which is central to Party improvement; as such, communists must not fear it, but engage in it openly.

<font color='grey' size='3'>Communists</font>
{Chapter 28 - Quotations 18}
A communist must be selfless, with the interests of the masses at heart. He must also possess a largeness of mind, as well as a practical, far-sighted mindset.

<font color='grey' size='3'>Cadres</font>
{Chapter 29 - Quotations 11}
Cadres, the instrument for uniting with and working for the people, must be leaders versed in Marxist–Leninism. 
They must have both guidance and the freedom to use their creative inititave in solving problems. 
Newer cadres and older cadres must work together with a comradely respect, learning from each other.

<font color='grey' size='3'>Youth</font>
{Chapter 30 - Quotations 7}
The Chinese Youth represent an active, vital force in China, to be drawn upon. 
At the same time, it is necessary to educate them, and for the Youth League to give special attention to their problems and interests.

<font color='grey' size='3'>Women</font>
{Chapter 31 - Quotations 7}
Women represent a great productive force in China, and equality among the sexes is one of the goals of communism. The multiple burdens which women must shoulder are to be eased.

<font color='grey' size='3'>Culture and Art</font>
{Chapter 32 - Quotations 8}
Literature and art are discussed with respect to communism, in an orthodox fashion. (Principally consisting of quotations from "Talks at the Yenan Forum on Literature and Art".)

<font color='grey' size='3'>Study</font>
{Chapter 33 - Quotations 16}
It is the responsibility of all to cultivate themselves, and study Marxism–Leninism deeply. 
It is also necessary for people to turn their attention to contemporary problems, along empirical lines.


<font color='white' size='4'>Historical Publication Notes</font>
Quotations from Chairman Mao Tse-tung was originally compiled by an office of the PLA Daily (People's Liberation Army Daily) as an inspirational political and military document. 
The initial publication covered 23 topics with 200 selected quotations by the Chairman of the Chinese Communist Party, and was entitled 200 Quotations from Chairman Mao. 
It was first given to delegates of a conference on 5 January 1964 who were asked to comment on it. 
In response to the views of the deputies and compilers of the book, the work was expanded to address 25 topics with 267 quotations, and the title was changed simply to Quotations from Chairman Mao Tse-Tung.
